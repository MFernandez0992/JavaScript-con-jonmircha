/* Undefined, Null & NaN */

// Undefined - Indica que no se ha inicializado una variable y que el valor está ausente
let indefinida;
console.log(indefinida);

// Null - Es un valor especial que el developer le asigna a una variable indicando que está vacía
let nulo = null;
console.log(nulo);

// NaN - Not a Number, JavaScript mostrará este tipo cuando se quiera hacer operaciones y detecte que alguna no es number
let noEsUnNumero = 'hola' * 2;
console.log(noEsUnNumero);